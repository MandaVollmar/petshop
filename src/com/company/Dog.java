package com.company;

public class Dog {
    private String name;
    private int age;
    private String color;

    public void setName (String nameOfDog) {
        name = nameOfDog;
    }
    public String getName (){
        return name;
    }
    public void setAge (int ageOfDog) {
        age = ageOfDog;
    }
    public int getAge (){
        return age;
    }
    public void setColor (String colorOfDog) {
        color = colorOfDog;
    }
    public String getColor (){
        return color;
    }
}
