package com.company;

public class Cat {
    private String name;
    private int age;
    private String color;

    public void setName (String nameOfCat) {
        name = nameOfCat;
    }
    public String getName (){
        return name;
    }
    public void setAge (int ageOfCat) {
        age = ageOfCat;
    }
    public int getAge (){
        return age;
    }
    public void setColor (String colorOfCat) {
        color = colorOfCat;
    }
    public String getColor (){
        return color;
    }
}
